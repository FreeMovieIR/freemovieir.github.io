window.i18n = {
    current: localStorage.getItem('site_lang') || 'fa',
    t: function (key) {
        return i18nData[this.current][key] || key;
    },
    setLanguage: function (lang) {
        if (i18nData[lang]) {
            this.current = lang;
            localStorage.setItem('site_lang', lang);
            document.documentElement.lang = lang;
            document.documentElement.dir = (lang === 'fa' || lang === 'ar') ? 'rtl' : 'ltr';
            location.reload();
        }
    }
};

// Auto-apply direction on load
(function () {
    const lang = window.i18n.current;
    document.documentElement.lang = lang;
    document.documentElement.dir = (lang === 'fa' || lang === 'ar') ? 'rtl' : 'ltr';
})();
